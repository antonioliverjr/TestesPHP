<?php 

function soma($a, $b){
    return $a + $b;
}

function subtracao($a, $b){
    return $a - $b;
}

function multiplicar($a, $b){
    return $a * $b;
}

function dividir($a, $b){
    return $a / $b;
}

function resto($a, $b){
    return $a % $b;
}

function potencia($a, $b){
    return $a ** $b;
}

?>